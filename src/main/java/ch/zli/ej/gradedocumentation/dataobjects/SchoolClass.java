package ch.zli.ej.gradedocumentation.dataobjects;

public class SchoolClass {

    private long id_;

    private String name_;

    private int year_;

    public long getId() {
        return id_;
    }

    public void setId(long id) {
        id_ = id;
    }

    public String getName() {
        return name_;
    }

    public void setName(String name) {
        name_ = name;
    }

    public int getYear() {
        return year_;
    }

    public void setYear(int year) {
        year_ = year;
    }
}
